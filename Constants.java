/*
* Constants interface provides some constants
* in a Tic Tac Toe game like the two kinds of
* marks: O and X as well as a space character.
* */
public interface Constants {
	static final char SPACE_CHAR = ' ';

	/*
	* mark for one of the two players
	* */
	static final char LETTER_O = 'O';

	/*
	* mark for the other player
	* */
	static final char LETTER_X = 'X';
}
